# Write a function named average_of_3 that accepts three integers as
# parameters and returns the average of the three integers as a number.
# For example, the call of average_of_3(4, 7, 13) returns 8.


def average_of_3(num1, num2, num3):
    return (num1 + num2 + num3) / 3


average_of_3(3, 5, 19)  # 9.0
average_of_3(7, -7, 3000)  # 1000.0
average_of_3(-1, -9, -5)  # -5.0

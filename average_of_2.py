# Write a function named average_of_2 that takes two integers as parameters
# and returns the average of the two integers. For example, the call of
# average_of_2(2, 9) should return 5.5.


def average_of_2(num1, num2):
    return (num1+num2) / 2


average_of_2(3, 5)  # 4.0
average_of_2(7, 6)  # 6.5
average_of_2(-1, -9)  # -5.0